public class LiteralsSample {
    public static void main(String[] args) {
        int dec = 12;
        int hex = 0xc;
        int oct = 014;
        float Fex = 583.45f;
        double Dex = 583.45;
        float Sn = 5.8345e2f;

        System.out.println(dec);
        System.out.println(hex);
        System.out.println(oct);
        System.out.println(Fex);
        System.out.println(Dex);
        System.out.println(Sn);
    }
}
